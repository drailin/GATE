//=======================================================
void gate::Cluster::Info(ostream& s) const{
//=======================================================

    s << "========= Cluster instance ======="<< std::endl;
    
    Hit::Info(s);

    s << "Number of hits:" << this->GetHits().size()<< std::endl;
    
    s << "=========================="<< std::endl;

}

//=======================================================
ostream& operator << (ostream& s, const gate::Cluster& c) {
//=======================================================

    c.Info(s);
  
    return s; 
}
